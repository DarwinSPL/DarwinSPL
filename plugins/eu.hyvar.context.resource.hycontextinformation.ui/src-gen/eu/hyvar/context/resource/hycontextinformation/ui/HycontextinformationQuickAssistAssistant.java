package eu.hyvar.context.resource.hycontextinformation.ui;

import org.eclipse.jface.text.AbstractReusableInformationControlCreator;
import org.eclipse.jface.text.DefaultInformationControl;
import org.eclipse.jface.text.DefaultInformationControl.IInformationPresenter;
import org.eclipse.jface.text.IInformationControl;
import org.eclipse.jface.text.quickassist.IQuickAssistAssistant;
import org.eclipse.jface.text.quickassist.IQuickAssistInvocationContext;
import org.eclipse.jface.text.quickassist.QuickAssistAssistant;
import org.eclipse.jface.text.source.Annotation;
import org.eclipse.swt.widgets.Shell;

public class HycontextinformationQuickAssistAssistant extends QuickAssistAssistant implements IQuickAssistAssistant {
	
	public HycontextinformationQuickAssistAssistant(eu.hyvar.context.resource.hycontextinformation.IHycontextinformationResourceProvider resourceProvider, eu.hyvar.context.resource.hycontextinformation.ui.IHycontextinformationAnnotationModelProvider annotationModelProvider) {
		setQuickAssistProcessor(new eu.hyvar.context.resource.hycontextinformation.ui.HycontextinformationQuickAssistProcessor(resourceProvider, annotationModelProvider));
		setInformationControlCreator(new AbstractReusableInformationControlCreator() {
			public IInformationControl doCreateInformationControl(Shell parent) {
				return new DefaultInformationControl(parent, (IInformationPresenter) null);
			}
		});
	}
	
	public boolean canAssist(IQuickAssistInvocationContext invocationContext) {
		return false;
	}
	
	public boolean canFix(Annotation annotation) {
		return true;
	}
	
}
